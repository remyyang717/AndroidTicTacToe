六种判断格局吉凶状况：



吉：大安、速喜、小吉。其中小吉为最吉。

凶：留连、赤口、空亡。其中空亡为大凶。



当我们掐算占得某种格局时，就可以根据某格局的基本含义来分析判断所测事物的吉凶状况。首先可根据三吉三凶格局得知所测之事是吉、是凶的大概状况，再根据其格局的断辞进行详细分析结果。

如测失物得“速喜”，“速喜” 的含义是表示做事迅速、喜事来临，就可断定测失物为吉，有希望找回，即失物不会丢失，这是大趋势。具体应该怎样找、从何方去找、需要多长时间才能找回等等，这就要根据“速喜”的断辞进行具体分析。“速喜”的断辞曰：速喜喜来临，求财向南行，失物申午未，逢人路上寻，官事有福德，病者无祸侵，田家六畜吉，行人有信音。根据断辞含义，我们可以推断所测的失物要快速去找，方位为申午未方，根据十二地支方位图得知，未申为西南，午为正南，所以应从西南方或正南方这些方位上去找，就能够找到所失之物。速喜属火，火主急、主礼、迅速，所以应该立即去找，而不担误时间。还可以进一步推断下去，比如什么时间找到？所丢失的物品有多远？所丢东西有多少？或者所拾之人是什么模样等情况。那么什么时间能找到呢？根据速喜断辞 “失物申午未” ，可推断在申时、午时、未时找到，具体断那个时辰呢？要根据具体情况作具体判断。如未时来测，可断申时，而不能断午时，因为午时己过，不可能断明日午时。所丢失的物品有多远呢？也可同样按上述方法变通分析。速喜谋事主三、六、九，可推断在300米、900米或三里、六里、九里等地方去找。所丢钱物有多少呢，同样可断为300元，600元，900元，或者369元，也可断为30、60、90元等。至于是什么人所拾到等，可根据速喜属火主礼、为朱雀的特性来推断，此人为外表面色为红色，中等身材，因火主礼，所推断为对人有礼貌，性急，口材好，且口直心快之人等等。总之，要具体情况具体分析，同时亦要根据当时的需要作具体而不同的推断，切记死搬硬套，因为每次所测的情况、需要均不同，而每个人所测的要求也不同，只需要推断所问的内容和要求即可，求测者未涉及的没有问的内容，就不要去套搬断辞的内容，任何预测都有一个信息场的问题，凡不在当时求测者所需求的信息场之内的内容，也就不在判断的范围之内，也不一定推断推确，这是预测的信息灵感度问题。

同时，任何的推判均是一个方向和状况问题，没有一成不变的东西，要掌握变通之原则，易学的原理就在于变通，易经即有不易、变易的原则，变易是任何预测学的基本原则。所谓变通，就是根据时间、地点、环境、个性、以及所测之人和事的不同特征、属性和需求，进行针对性、选择性、合理性的分析、推断，而不照搬硬套，答非所问，张官李戴等。比如上例测失物得速喜一例，如求测者只问能否找到所丢之物和在什么地方去找才能找到等，就没有必要再方析其它的情况，因为其它的情况不在当时求测者所需的信息场之中，如果硬套分析下去，推断就不一定准确。

变通的原则还有一点也十分重要，就是要善于观察事物的本质，善于运用易学的基本原则去分析、挖掘事物许许多多、不同持点、方方面面的东面，运用联想、扩展、引伸等推导方法将古时断辞中未涉及到的现代社会的各种含义、特征等推断出来，丰富其内涵，只有这样才能更好地发挥小六壬掌诀的神奇功效，为当代社会服务。



