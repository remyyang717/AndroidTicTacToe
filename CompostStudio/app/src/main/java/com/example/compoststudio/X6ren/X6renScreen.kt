第四卷：《小六壬掌》判断篇



《小六壬掌》的 “大安、留连、速喜、赤口、小吉、空亡” 六种判断格局，是判断事物的六种基本状态，它包括了所测事物的全部内容，是对其预测结果的高度概括，在实际运用中，可根据这六种格局的基本含义进行引伸、扩展和联想，这样才能适应当今社会生活和人们预测范围的需要。



这六种判断格局的基本含义是：



大安：表示光明正大、平平安安；

留连：表示岁月漫长、纠缠绵绵；

速喜：表示做事迅速、喜事来临；

赤口：表示口舌是非、破败多端；

小吉：表示凡事可谋、多吉多利；

