第六卷：《小六壬掌》实例篇



任何预测方法都在于实践，实践才是检验正确与否的唯一标准，在实践中不断总结、探讨经验是提高预测准确率的唯一途径。下面一些实例可作为研究探究之抛砖引钰。

例1：6月12日辰时，一朋友去成都办事，不知能否顺利，一路是否平安？

解：首先根据掌法从手掌大安处起正月，数至空亡处为6月，再从空亡处起初一，数至小吉处为12日，以小吉处起子时，辰时在速喜宫。根据速喜的断曰，可断去成都办事吉利，一顺平安，不会发生不愉快之事。断辞曰：谋事为3、6、9，断此去成都办事至少有三个以上方面的事办，或三方面的人和事有关，所办之三方面事有一位女士、二位男士有关系。因为3、6、9三个数3、9为阳，6为阴，所以断之。6为富贵、豪门之数，故断这位女士为一位老板。同行的至少有3-6人，总计共需要9小时以上。结果情况：早上8.30出发去成都，路途十分顺利，同车的刚刚6人，到成都后上午立即找到了要办事之人，而办事之人是一位企业的女老板，并且立即办成了所办的主要之事，中午又预约与一位在成都工作的老乡见面，下午见到了中断一年多未有联系的成都老朋友，三人在一起到茶楼共叙友情，交流感情，互托办事等等，并且三位一起在餐馆进餐，到晚上10.50亥时左右才返回，从早辰8.30出发到晚上亥时返回中间总共6个时辰。整个外出的过程均和速喜断辞数理3、6、9有紧密的联系，这是不是偶然的现象呢，读者可去作仔细地思索与推敲。

例2：12月11丑时，友人从成都来电测丢失钱包，内有作证、银行卡、身份证等，十分着急，看能否找回？

解：从手掌大安处起正月，12月在空亡宫，再从从空起初一，11在留连宫，又从留连宫起子时，丑时在小吉宫，根据小吉断辞：小吉最吉昌，路上好商良，阳人来报喜，失物在坤方，行人立便至，交关真是强，凡事皆和合，病者事无仿。可立刻判断：此丢失的钱包能找回，失物在“坤” ，坤为家宅，且为西南方，也就是说钱包丢在家里西南方房间里。我马上在电话中告之这位友人，不要急，在家里西南房间找。大约20分钟后，这位友人来电高兴告之，丢失的钱包己找到了，对我的神奇预测表示感谢！



第七卷：《小六壬掌》变通篇



何谓变通？变通即相通致变，就是说世了间万事万物都有相通之处，并可以互相转变，互相通溶。小六壬掌的断判格局只有六种，要将万事万物都包括在内是不可能的，许多判断只能靠变通的方法来实现。具体怎样变通，没有固定的模式，这里仅提供一种思路，大家可顺着这种思路去大胆的想象，跟着感觉走，成功在前头！

小六壬掌的格局名称、象数和断辞等除表示一定的吉凶内容外，还可用于变通断事。

1、格局名称的变通

“大安”：可以变通理解为大可安心〔如占测不放心之事〕、大人安全、领导安全、老大安全、大哥、大姐安全等等。

“留连”：可理解为牵连、连绵不断、纠缠绵绵、留了一面、藕断丝连、细水长流等等。

“速喜”：可理解为有喜事、喜来临、快快起〔谐音〕、立即起、立即去、畜起来等等。

“赤口”：口舌是非、七个口、一起走〔谐音〕、赤色之口、赤露着口、一无所有、张口争吵等等。

“小吉”：好吉利、多吉、小姐吉、小人吉利、小事吉利、小人得利、小小的吉利、小有吉利等等。

“空亡”： 空欢喜、空望、劳而无功、一事无成、空手无望、空洞死亡、空中无望、亡命之徒、亏损无望等等。



2、象数的变通



六种格局的象数也可同样作变通处理，比如：一、五、七，如断时间，可理解一点、五点、七点，或一个时辰、五个、七个时辰，或者为157分等等，如断数字，也可按同样的道理去变通处理。

其它五种格局的象数也可同样按这种思路去分析、理解。



3、断辞的变通



小六壬掌的断辞同样可以进行变通。

1、“大安” 断辞：“身不动时，属木青龙，凡谋事主一、五、七” 。如测行人，“身不动时” 可理解为行人尚未动身，测一般事可理解为事情尚未开始，处于未动阶段。“属木青龙” 一句可变通理解为木质东西、木材、树木、木姓之人等，“青龙” 为六神，可从其姓格、特征上去分析，选其有用、有结合之处进行判断。



2、“留连” 断辞：“卒未归时，属水，为玄武，凡谋事主二、八、十。” “卒未归时” 指时机尚未到，所以“求谋曰未明” ，凡事宜暂缓。



3、“速喜” 断辞：“人便至时，属火，为朱雀，凡谋事主三、六、九”。“人便至时” 表示时机到，应抓住时机立即去办，如测行人则所测之人马上就到。



4、“赤口” 断辞：“官事凶时，属金，为白虎，凡谋事主四、七、十。” “官事凶时” 则是一个遇官非的凶兆格局，所以“鸡犬多作怪”，要提防小人作怪，并且“官非切要防”。



5、“小吉”断辞：“人来喜时，属木，为六合，凡谋事主一、五、七。” “人来喜时” 指有人来报喜，六合也主事事顺心、合和，成功在望。



6、“空亡”断辞：“音信稀时，属土，为勾陈，凡谋事主一、五、七。” “音信稀时” 表示查无音信，没有结果，故而“空亡事不长，阴人小乖张”，阴为女人、小人，有女人或小人捣乱，要特别注意。



